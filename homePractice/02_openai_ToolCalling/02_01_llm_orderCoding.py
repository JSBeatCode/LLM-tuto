# 1. [프롬프트 설계]
#    - 사용할 API 이름(네이버 뉴스 API)을 명시할수록 정확도 향상
#    - 함수 시그니처(get_news(query))와 출력 포맷을 구체적으로 지정
#    - 원하는 동작의 예시 코드 구조를 포함하면 더 정밀한 결과 생성
#
# 2. [system 역할 활용]
#    - "설명 없이 코드만 출력" 지시로 마크다운 설명 등 불필요한 텍스트 제거
#    - system 메시지는 LLM의 전반적인 역할과 출력 형식을 제어하는 데 효과적
#
# 3. [temperature=0]
#    - 0에 가까울수록 결정적(deterministic)인 출력 → 코드 생성에 적합
#    - 창의적 글쓰기에는 높은 temperature, 코드/사실 답변에는 낮은 값 권장
#
# 4. [생성 코드 활용 패턴]
#    - 생성된 코드를 복사해 별도 파일로 저장 후 API 키만 채워 바로 실행 가능
